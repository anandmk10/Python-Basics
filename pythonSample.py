if x == '+' :
    a=int(input("Enter the first value"))
    b=int(input("Enter the second value"))
    print(a + b)
elif x== '-':
    a=int(input("Enter the first value"))
    b=int(input("Enter the second value"))  
    print(a - b)
elif x== '*':
    a=int(input("Enter the first value"))
    b=int(input("Enter the second value"))    
    print(a * b)
elif x== '/':
    a=int(input("Enter the first value"))
    b=int(input("Enter the second value"))   
    print(a / b)
elif x== '%':
    a=int(input("Enter the first value"))
    b=int(input("Enter the second value"))    
    print(a % b)
